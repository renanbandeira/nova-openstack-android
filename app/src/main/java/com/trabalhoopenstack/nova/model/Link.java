package com.trabalhoopenstack.nova.model;

public class Link {
  String href;
  String rel;

  public Link() {
  }

  public String getHref() {
    return href;
  }

  public void setHref(String href) {
    this.href = href;
  }

  public String getRel() {
    return rel;
  }

  public void setRel(String rel) {
    this.rel = rel;
  }
}
