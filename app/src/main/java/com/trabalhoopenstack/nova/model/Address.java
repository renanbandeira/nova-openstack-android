package com.trabalhoopenstack.nova.model;

public class Address {
  String addr;
  String version;

  public Address() {
  }

  public String getAddr() {
    return addr;
  }

  public void setAddr(String addr) {
    this.addr = addr;
  }

  public String getVersion() {
    return version;
  }

  public void setVersion(String version) {
    this.version = version;
  }
}
